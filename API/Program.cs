        Version = "v1",
        Description = "REST API РґР»СЏ РїР»Р°С‚С„РѕСЂРјС‹ РѕР±СЂР°Р·РѕРІР°С‚РµР»СЊРЅРѕРіРѕ Р±Р°СЂС‚РµСЂР°"
